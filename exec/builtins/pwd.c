#include "builtins.h"

int	ft_pwd(t_data *data)
{
	int	i;

	i = 0;
	while(data->cwd[i])
	{
		write(1,&data->cwd[i],1);
		i++;
	}
	write(1,"\n",1);
	return (0);
}
